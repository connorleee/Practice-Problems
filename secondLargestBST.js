class BinaryTreeNode {
    constructor(value) {
        this.value = value;
        this.left = null;
        this.right = null;
    }

    insertLeft(value) {
        this.left = new BinaryTreeNode(value);
        return this.left;
    }

    insertRight(value) {
        this.right = new BinaryTreeNode(value);
        return this.right;
    }
}

function findSecondLargest(treeRoot) {

    // Find the second largest item in the binary search tree

    if (!treeRoot) {
        throw new Error("There must be a root node!")
    }

    // If there is only one node or two nodes and there is no right node
    if (!treeRoot.right) {
        if (treeRoot.left) {
            return treeRoot.left.value;
        } else {
            throw new Error("only one node");
        }
    }

    while(treeRoot){

        // Largest node has a left subtree or only a left child
        if(treeRoot.left && !treeRoot.right){
            if(treeRoot.left.right){
                return findLargest(treeRoot.left).value;
            }
    
            return treeRoot.left.value;
        }
    
        // Largest node has no children --> return parent node value
        if ( treeRoot.right && !treeRoot.right.right && !treeRoot.right.left) {
            return treeRoot.value
        }
    
        treeRoot = treeRoot.right;
    }
}

function findLargest(treeNode) {
    while (treeNode.right) {
        treeNode = treeNode.right
    }

    return treeNode
}



